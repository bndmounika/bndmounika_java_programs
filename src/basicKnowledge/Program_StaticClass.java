package basicKnowledge;

public class Program_StaticClass {

	public static void main(String[] args) {
		Program1.ram();
		Program1.sita();

	}
	
	static class Program1
	{
		static void ram()
		{
			System.out.println(" Hi");
		}
		static void sita()
		{
			System.out.println("hello");
		}
		
	}

}
